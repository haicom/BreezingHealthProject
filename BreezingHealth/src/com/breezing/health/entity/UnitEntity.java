package com.breezing.health.entity;

public class UnitEntity {

    private String unitType;
    private String unitName;
    private String unitUnifyData;
    private String unitObtainData;
    public String getUnitType() {
        return unitType;
    }
    public void setUnitType(String unitType) {
        this.unitType = unitType;
    }
    public String getUnitName() {
        return unitName;
    }
    public void setUnitName(String unitName) {
        this.unitName = unitName;
    }
    public String getUnitUnifyData() {
        return unitUnifyData;
    }
    public void setUnitUnifyData(String unitUnifyData) {
        this.unitUnifyData = unitUnifyData;
    }
    public String getUnitObtainData() {
        return unitObtainData;
    }
    public void setUnitObtainData(String unitObtainData) {
        this.unitObtainData = unitObtainData;
    }
    
    
    
}
